/* 4. Reescreva o exercício 3, utilizando apenas o SWITCH. */

switch (prompt(" Olá, digite um número de 1 a 7: ")){

case '1': 
alert("É Domingo! ");
break

case '2': 
alert("É Segunda-Feira! ");
break

case '3': 
alert("É Terça-Feira! ");
break

case '4': 
alert("É Quarta-Feira! ");
break

case '5': 
alert("É Quinta-Feira! ");
break

case '6': 
alert("É Sexta-Feira! ");
break

case '7': 
alert("É Sábado! ");
break

default: 
alert("Olá, dia não encontrado! ")
break

}